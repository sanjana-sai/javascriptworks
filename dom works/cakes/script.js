function populateSelectBox(){

  let choices=new Set(cakes.map(c=>c.difficulty) )  
  
  let selectBox=document.querySelector("#selectBox")

  for(ch of choices){

    let optionTag=document.createElement("option")

    optionTag.text=ch

    optionTag.value=ch

    selectBox.appendChild(optionTag)
  }

}